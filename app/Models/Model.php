<?php

abstract class Model
{
    protected ?int $id = null;

    abstract public function getTableName() : string;

    /**
     * @return int|null
     */
    public function getId(): ?int
    {
        return $this->id;
    }

    /**
     * @param int|null $id
     */
    public function setId(?int $id): void
    {
        $this->id = $id;
    }
}
